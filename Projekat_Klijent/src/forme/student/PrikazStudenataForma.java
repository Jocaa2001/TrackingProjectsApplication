    private void jButtonPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPretraziActionPerformed
        int brojIndeksa;
               if(jTextFieldBrInd.getText() == null ||jTextFieldBrInd.getText().isEmpty())
                   brojIndeksa = 0;
               else 
               brojIndeksa = Integer.valueOf(jTextFieldBrInd.getText());
               String ime = jTextFieldIme.getText();
               String prezime = jTextFieldPrezime.getText();
               
                ModelTabeleStudent mts = (ModelTabeleStudent) jTableStudenti.getModel();
                mts.findStudent(brojIndeksa, ime,prezime);
                if(mts.getLista().size() == 0)
                    JOptionPane.showMessageDialog(this, "Систем не може да нађе студенте по задатим вредностима");
                else
                    JOptionPane.showMessageDialog(this, "Систем је нашао студенте по задатим вредностима");
                
        
        
    }//GEN-LAST:event_jButtonPretraziActionPerformed

