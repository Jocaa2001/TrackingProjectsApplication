    private void jButtonDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDetaljiActionPerformed
         int red = jTableStudenti.getSelectedRow();
                if(red == -1){
                     JOptionPane.showMessageDialog(this, "изаберите студента из табеле","Грешка", JOptionPane.INFORMATION_MESSAGE);
                     return;
                }
                ModelTabeleStudent mts = (ModelTabeleStudent) jTableStudenti.getModel();
                Student s = mts.getLista().get(red);
                
                DodajStudentaForma dsf = new DodajStudentaForma(null, true,s,null);
                
                dsf.pripremiFormu(ModStudent.detalji);
                 JOptionPane.showMessageDialog(this, "Систем је учитао студента", "Неуспех", JOptionPane.INFORMATION_MESSAGE);
                dsf.setVisible(true);
    }//GEN-LAST:event_jButtonDetaljiActionPerformed

    
