    private void jButtonPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPretraziActionPerformed
        
        int espb;
            if(jTextFieldEspb.getText() == null || jTextFieldEspb.getText().isEmpty())
                espb = 0;
            else 
                espb = Integer.valueOf(jTextFieldEspb.getText()); 
             String naziv = jTextFieldNaziv.getText(); 
             ModelTabelePredmet mtp = (ModelTabelePredmet) jTablePredmeti.getModel();
             if(jComboBox1.getSelectedIndex() == -1 || jComboBox1.getSelectedIndex() == 2)
             mtp.pretraziNoBool(espb, naziv);
             else{
             boolean obavezan = jComboBox1.getSelectedItem().equals("Da");
             mtp.findPredmet(espb,naziv,obavezan);    
              }
             if(mtp.getLista().size() == 0)
                JOptionPane.showMessageDialog(this, "Систем не може да нађе предмете по задатим вредностима");
             else
                JOptionPane.showMessageDialog(this, "Систем је нашао предмете по задатим вредностима");

    }//GEN-LAST:event_jButtonPretraziActionPerformed

