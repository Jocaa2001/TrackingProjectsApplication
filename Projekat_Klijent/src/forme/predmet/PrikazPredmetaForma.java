    private void jButtonDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDetaljiActionPerformed
        
          int red = jTablePredmeti.getSelectedRow();
                if(red == -1){
                     JOptionPane.showMessageDialog(this, "Изаберите предмет из табеле","Грешка", JOptionPane.INFORMATION_MESSAGE);
                     return;
                }
                
                ModelTabelePredmet mtp = (ModelTabelePredmet) jTablePredmeti.getModel();
                Predmet p = mtp.getLista().get(red);
               
                ObrisiPredmetForma opf = new ObrisiPredmetForma(null, true,p);
                opf.pripremiFormu(ModPredmet.pronadji);
                opf.setVisible(true);
    }//GEN-LAST:event_jButtonDetaljiActionPerformed

