    public void pripremiFormu() {
        List<Predmet> predmeti = controller.Controller.getInstance().loadListPredmeti();
        ModelTabelePredmet mtp = new ModelTabelePredmet(predmeti);
        jTablePredmeti.setModel(mtp);
    }

    public void setMode(ModPredmet modPredmet) {
        switch (modPredmet) {
            case pronadji:
                jButtonDetalji.setVisible(true);
                jButtonObrisi.setVisible(false);
                break;
            case obrisi:
                jButtonDetalji.setVisible(false);
                jButtonObrisi.setVisible(true);
                break;
            default:
                throw new AssertionError();
        }
    }
