    private void jButtonPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPretraziActionPerformed
 
    Profesor prof;
    int ind = jComboBoxProfesorPretrazi.getSelectedIndex();
    
    ArrayList<Profesor> profesori = (ArrayList<Profesor>) controller.Controller.getInstance().loadListProfesor();
        
    if(ind == -1){
       prof = null;
    }else{
         prof = profesori.get(ind);
    }
    
    Predmet pred = (Predmet) jComboBoxPredmetPretrazi.getSelectedItem();
    Student stud = (Student) jComboBoxStudent.getSelectedItem();
    String tema = jTextFieldTema.getText().trim();
    String datum = jTextFieldDatum.getText().trim();
    java.util.Date Datum = null;

    // Parsiranje datuma ako je polje popunjeno
    if (!datum.isEmpty()) {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy.MM.dd");
        try {
            Datum = sdf.parse(datum);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Грешка у датуму");
            return;  // Prekida ako datum nije ispravan
        }
    }
    
    // Parsiranje broja poena ako je polje popunjeno
    Integer brojPoena = null;
    if (!jTextFieldBrojPoena.getText().trim().isEmpty()) {
        try {
            brojPoena = Integer.parseInt(jTextFieldBrojPoena.getText().trim());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Грешка у уносу броја поена");
            return;  // Prekida ako broj poena nije ispravan
        }
    }
    
    // Poziv metode za pretragu
    ModelTabeleProjekti mtp = (ModelTabeleProjekti) jTableProjekti.getModel();
     System.out.println("Unete vrednosti:");
    System.out.println("Tema: " + tema);
    System.out.println("Datum: " + datum);
    System.out.println("Broj poena: " + brojPoena);
    System.out.println("Profesor: " + prof);
    System.out.println("Predmet: " + pred);
    System.out.println("student: " + stud);
    
    
    mtp.pretrazi(prof, pred, stud, tema.isEmpty() ? null : tema, Datum, brojPoena);
    
    // Ažuriranje modela tabele
    if (mtp.getLista().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Систем не може да нађе пројекте по задатим вредностима");
    } else {
        JOptionPane.showMessageDialog(this, "Систем је нашао пројекте по задатим вредностима");
        }
    }//GEN-LAST:event_jButtonPretraziActionPerformed

