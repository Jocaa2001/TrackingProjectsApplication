    private void populateComboBoxes() {
        
         List<Predmet> predmeti = controller.Controller.getInstance().loadListPredmeti();
         
        for (Predmet p : predmeti) {
            jComboBoxPredmetPretrazi.addItem(p);
        }
        
         for (Predmet p : predmeti) {
            jComboBoxPredmetDodaj.addItem(p);
        }
        List<Profesor> profesori = controller.Controller.getInstance().loadListProfesor();
        for (Profesor p : profesori) {
            jComboBoxProfesorDodaj.addItem(p);
        }
        
        for (Profesor p : profesori) {
            jComboBoxProfesorPretrazi.addItem(p);
        }
        
        List<Student> studenti = controller.Controller.getInstance().loadListStudenti();
        for (Student s : studenti) {
            jComboBoxStudent.addItem(s);
        }
        
        jComboBoxPredmetPretrazi.addItem(null);
        jComboBoxStudent.addItem(null);
        jComboBoxProfesorPretrazi.addItem(null);
        
    }
